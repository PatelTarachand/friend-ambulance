require_once 'includes/auth.php';

// Redirect if already logged in
if (AdminAuth::isLoggedIn()) {
    // Clear any output buffer to prevent header issues
    if (ob_get_level()) {
        ob_end_clean();
    }
    header('Location: dashboard.php');
